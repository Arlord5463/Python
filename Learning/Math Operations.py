# Операции с числами
a = 7
b = 2
print(a + b)  # Сложение
print(a - b)  # Вычитание
print(a * b)  # Умножение
print(a / b)  # Вещественное деление: 7 / 2 = 3.5, но -7 / 2 = -3.5
print(a // b)   # Деление нацело (Целочисленное деление): 7 // 2 = 3, но -7 // 2 = -4
print(a % b)  # Взятие остатка от деления: 7 % 2 = 1 и -7 % 2 = 1
print(a ** b)  # Возведение в степень

# Операции со строками
s1 = '7'
s2 = '2'
count = 5
print(s1 + s2)
print(s1 * count)
